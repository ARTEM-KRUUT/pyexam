def main(h:str):
    v = int(h, 16)
    o1 = v & 0b111111111
    o2 = (v >> 10) & 0b1111
    o3 = (v >> 14) & 0b1
    o4 = (v >> 15) & 0b1111
    o5 = (v >> 19) & 0b111111
    o6 = (v >> 25) & 0b11111111111

    result = o1 | (o2 << 10) | (o5 << 14) | (o4 << 20) | (o6 << 24) | (o3 << 34)
    return str(result) #транскодирование

